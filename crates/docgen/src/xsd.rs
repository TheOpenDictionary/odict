fn media_url(src: &str) -> MediaURL {
    MediaURL {
        src: src.to_string(),
        mime_type: Some("application/octet-stream".to_string()),
        description: Some("media".to_string()),
    }
}

fn reflection_error(error: ReflectionError) -> anyhow::Error {
    anyhow::anyhow!("{}\n{}", error, error.explanation().trim())
}

fn preferred_type_order() -> &'static [&'static str] {
    &[
        "url",
        "Pronunciation",
        "translation",
        "example",
        "note",
        "Definition",
        "group",
        "form",
        "sense",
        "ety",
        "entry",
        "dictionary",
    ]
}

fn should_emit_complex_type(container: &ContainerFormat) -> bool {
    matches!(container, ContainerFormat::Struct(_))
}

fn write_complex_type(
    output: &mut String,
    name: &str,
    container: &ContainerFormat,
    registry: &Registry,
) -> Result<()> {
    let ContainerFormat::Struct(fields) = container else {
        return Ok(());
    };

    let child_fields: Vec<_> = fields.iter().filter(|field| !is_attribute(field)).collect();

    let _ = writeln!(
        output,
        "  <xs:complexType name=\"{}\">",
        xsd_type_name(name)
    );

    if !child_fields.is_empty() {
        output.push_str("    <xs:sequence>\n");
        for field in child_fields {
            write_child_field(output, field, registry)?;
        }
        output.push_str("    </xs:sequence>\n");
    }

    for field in fields.iter().filter(|field| is_attribute(field)) {
        write_attribute(output, name, field, registry)?;
    }

    output.push_str("  </xs:complexType>\n\n");

    Ok(())
}

fn write_child_field(
    output: &mut String,
    field: &Named<Format>,
    registry: &Registry,
) -> Result<()> {
    if field.name == "$value" {
        return write_value_field(output, &field.value, registry);
    }

    let (format, min_occurs, max_occurs) = element_occurrence(&field.value);
    let element_type = xsd_format_type(format, registry)?;

    let _ = writeln!(
        output,
        "      <xs:element name=\"{}\" type=\"{}\" minOccurs=\"{}\" maxOccurs=\"{}\"/>",
        field.name, element_type, min_occurs, max_occurs
    );

    Ok(())
}

fn write_value_field(output: &mut String, format: &Format, registry: &Registry) -> Result<()> {
    let (format, min_occurs, max_occurs) = element_occurrence(format);
    let Format::TypeName(type_name) = format else {
        bail!("unsupported $value field format: {format:?}");
    };

    let Some(ContainerFormat::Enum(variants)) = registry.get(type_name) else {
        bail!("unsupported $value type: {type_name}");
    };

    let _ = writeln!(
        output,
        "      <xs:choice minOccurs=\"{}\" maxOccurs=\"{}\">",
        min_occurs, max_occurs
    );

    for variant in variants.values() {
        write_choice_variant(output, variant, registry)?;
    }

    output.push_str("      </xs:choice>\n");

    Ok(())
}

fn write_choice_variant(
    output: &mut String,
    variant: &Named<VariantFormat>,
    registry: &Registry,
) -> Result<()> {
    let element_type = match &variant.value {
        VariantFormat::NewType(format) => xsd_format_type(format, registry)?,
        other => bail!("unsupported XML choice variant {}: {other:?}", variant.name),
    };

    let _ = writeln!(
        output,
        "        <xs:element name=\"{}\" type=\"{}\"/>",
        variant.name, element_type
    );

    Ok(())
}

fn write_attribute(
    output: &mut String,
    parent_name: &str,
    field: &Named<Format>,
    registry: &Registry,
) -> Result<()> {
    let attribute_name = field.name.trim_start_matches('@');
    let attribute_type = xsd_format_type(strip_option(&field.value), registry)?;
    let use_attribute = if is_required_attribute(parent_name, field) {
        " use=\"required\""
    } else {
        ""
    };

    let _ = writeln!(
        output,
        "    <xs:attribute name=\"{}\" type=\"{}\"{}/>",
        attribute_name, attribute_type, use_attribute
    );

    Ok(())
}

fn is_required_attribute(parent_name: &str, field: &Named<Format>) -> bool {
    !matches!(field.value, Format::Option(_)) && !is_defaulted_attribute(parent_name, &field.name)
}

fn is_defaulted_attribute(parent_name: &str, field_name: &str) -> bool {
    matches!(
        (parent_name, field_name),
        ("Dictionary" | "dictionary", "@id") | ("Sense" | "sense", "@pos")
    )
}

fn element_occurrence(format: &Format) -> (&Format, &'static str, &'static str) {
    match format {
        Format::Option(inner) => (inner, "0", "1"),
        Format::Seq(inner) => (inner, "0", "unbounded"),
        _ => (format, "1", "1"),
    }
}

fn strip_option(format: &Format) -> &Format {
    match format {
        Format::Option(inner) => inner,
        _ => format,
    }
}

fn xsd_format_type(format: &Format, registry: &Registry) -> Result<String> {
    match format {
        Format::Bool => Ok("xs:boolean".to_string()),
        Format::I8 | Format::I16 | Format::I32 | Format::I64 | Format::I128 => {
            Ok("xs:integer".to_string())
        }
        Format::U8 | Format::U16 => Ok("xs:unsignedShort".to_string()),
        Format::U32 => Ok("xs:unsignedInt".to_string()),
        Format::U64 | Format::U128 => Ok("xs:unsignedLong".to_string()),
        Format::F32 | Format::F64 => Ok("xs:decimal".to_string()),
        Format::Char | Format::Str => Ok("xs:string".to_string()),
        Format::Option(inner) | Format::Seq(inner) => xsd_format_type(inner, registry),
        Format::TypeName(type_name) => xsd_type_reference(type_name, registry),
        other => bail!("unsupported XSD format: {other:?}"),
    }
}

fn xsd_type_reference(type_name: &str, registry: &Registry) -> Result<String> {
    let Some(container) = registry.get(type_name) else {
        bail!("unknown schema type: {type_name}");
    };

    match container {
        ContainerFormat::NewTypeStruct(format) => xsd_format_type(format, registry),
        ContainerFormat::Enum(variants) if is_open_string_enum(variants) => {
            Ok("xs:string".to_string())
        }
        ContainerFormat::Struct(_) => Ok(xsd_type_name(type_name)),
        ContainerFormat::Enum(_) => Ok("xs:string".to_string()),
        other => bail!("unsupported schema type {type_name}: {other:?}"),
    }
}

fn is_open_string_enum(variants: &BTreeMap<u32, Named<VariantFormat>>) -> bool {
    variants.values().any(|variant| {
        variant.name == "Other"
            && matches!(variant.value, VariantFormat::NewType(ref format) if **format == Format::Str)
    })
}

fn is_attribute(field: &Named<Format>) -> bool {
    field.name.starts_with('@')
}

fn xsd_type_name(name: &str) -> String {
    match name {
        "MediaURL" => "mediaUrlType".to_string(),
        _ => format!("{}Type", lower_camel(name)),
    }
}

fn lower_camel(name: &str) -> String {
    let mut chars = name.chars();
    let Some(first) = chars.next() else {
        return String::new();
    };

    let mut output = String::new();
    output.push(first.to_ascii_lowercase());
    output.extend(chars);
    output
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generated_schema_contains_current_xml_model() {
        let schema = render_schema().unwrap();

        assert!(schema.contains("<xs:element name=\"media\" type=\"urlType\""));
        assert!(schema.contains("<xs:attribute name=\"rank\" type=\"xs:unsignedInt\""));
        assert!(schema.contains("<xs:attribute name=\"lemma\" type=\"xs:string\""));
        assert!(schema.contains("<xs:element name=\"translation\" type=\"translationType\""));
        assert!(schema.contains("<xs:element name=\"form\" type=\"formType\""));
        assert!(schema.contains("<xs:attribute name=\"id\" type=\"xs:string\"/>"));
        assert!(schema.contains("<xs:attribute name=\"pos\" type=\"xs:string\"/>"));
    }
}
