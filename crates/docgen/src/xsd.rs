use std::collections::{BTreeMap, BTreeSet};
use std::fmt::Write;
use std::path::Path;

use anyhow::{Context, Result, bail};
use odict::schema::{
    Definition, DefinitionType, Dictionary, Entry, EntryRef, EntrySet, Etymology, Example, Form,
    FormKind, Group, MediaURL, Note, PartOfSpeech, Pronunciation, PronunciationKind, Sense,
    SenseSet, Translation,
};
use serde_reflection::{
    ContainerFormat, Error as ReflectionError, Format, Named, Registry, Samples, Tracer,
    TracerConfig, VariantFormat,
};

pub fn generate_schema(output_path: impl AsRef<Path>) -> Result<()> {
    let schema = render_schema()?;
    let output_path = output_path.as_ref();

    if let Some(parent) = output_path.parent() {
        std::fs::create_dir_all(parent).with_context(|| {
            format!(
                "failed to create schema output directory {}",
                parent.display()
            )
        })?;
    }

    std::fs::write(output_path, schema)
        .with_context(|| format!("failed to write schema to {}", output_path.display()))?;

    Ok(())
}

fn render_schema() -> Result<String> {
    let registry = trace_schema()?;
    let mut output = String::new();
    let mut emitted = BTreeSet::new();

    output.push_str(
        "<xs:schema attributeFormDefault=\"unqualified\" elementFormDefault=\"qualified\"\n",
    );
    output.push_str("  xmlns:xs=\"http://www.w3.org/2001/XMLSchema\">\n");
    output.push_str(
        "  <!-- This file is auto-generated by `mise docgen` - do not edit manually. -->\n\n",
    );

    for &name in preferred_type_order() {
        if let Some(container) = registry.get(name)
            && should_emit_complex_type(container)
        {
            write_complex_type(&mut output, name, container, &registry)?;
            emitted.insert(name.to_string());
        }
    }

    for (name, container) in &registry {
        if emitted.contains(name) || !should_emit_complex_type(container) {
            continue;
        }

        write_complex_type(&mut output, name, container, &registry)?;
    }

    output.push_str("  <xs:element name=\"dictionary\" type=\"dictionaryType\"/>\n");
    output.push_str("</xs:schema>\n");

    Ok(output)
}

fn trace_schema() -> Result<Registry> {
    let mut tracer = Tracer::new(TracerConfig::default().is_human_readable(true));
    let mut samples = Samples::new();
    let sample = sample_dictionary();

    tracer
        .trace_value(&mut samples, &sample)
        .map_err(reflection_error)?;

    tracer.registry().map_err(reflection_error)
}

